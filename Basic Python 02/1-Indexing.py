# creating a string
a = "Million Coders"
print(a)

# Index starts from 0 left to right side
# To print 1st character of string "M"
print(a[0])

# To know the lenght of string indexes use len() function
# It will print the number of characters of "Million Coders"
print(len(a))

# To print "n" from "Million Coders"
print(a[6])


# To print the word "Coders"
# C starts from 8 and s ends on 13
# It skips the last index of the string so we're using 14 so it should not ski
print(a[8:14])

# To print C using -
print(a[-6])

# To print Coders using -
print(a[-6:14])
